.MODEL SMALL
.STACK 100H
.DATA
MSSG1 DB 'ENTER FIRST NUMBER : $'
MSSG2 DB 0DH,0AH,'ENTER SECOND NUMBER : $'
MSSG3 DB 0DH,0AH,'FINAL OUTPUT : $'
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
LEA DX,MSSG1
MOV AH,9 ;PRINT FIRST MESSSAGE
INT 21H
MOV AH,1
INT 21H ;FIRST INPUT
MOV BL,AL

MOV AH,2
MOV DL,0DH
INT 21H ;FOR NEW LINE
MOV DL,0AH
INT 21H
LEA DX,MSSG2
MOV AH,9 ;PRINT SECOND MESSSAGE
INT 21H
MOV AH,1
INT 21H ;SECOND INPUT
MOV BH,AL
MOV AH,2
MOV DL,0DH
INT 21H ;FOR NEW LINE
MOV DL,0AH
INT 21H

LEA DX,MSSG3
MOV AH,9 ;PRINT MESSSAGE 3
INT 21H


MOV AH,2
ADD BL,BH
SUB BL,48 ;SUM OF TWO NUMBERS
MOV DL,BL
INT 21H
MOV AH,4CH
INT 21H
END MAIN