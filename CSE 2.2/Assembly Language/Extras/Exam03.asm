.MODEL SMALL
.STACK 100H

.DATA



.CODE

MAIN PROC
		
	;COUNT NUMBER OF 1S IN BX
	
	MOV BX, 10101010101B
	
	MOV AL, 0
	
	MOV CX, 16
	
	START:
	
	ROR BX, 1
	
	JNC SKIP
	INC AL
	
	SKIP:
	LOOP START
	
	ADD AL, 48
	
	MOV AH, 2
	MOV DL, AL
	INT 21H
	
	DONE:
	
	MOV AH, 4CH
	INT 21H
	
	MAIN ENDP

END MAIN